public class bitwiseOperators {
    public static void main(String[] args) {

        // AND operator
        System.out.println(8&5);

        // OR operator
        System.out.println(8|5);

        // XOR operator
        System.out.println(8^5);

        // NOT (One's compliment) operator
        System.out.println(~5);

        // left shift operator
        System.out.println(5<<1);

        // right shift operator
        System.out.println(~5>>1);

    }
}
