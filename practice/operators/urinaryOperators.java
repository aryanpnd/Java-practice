public class urinaryOperators {
    public static void main(String[] args) {
        //uninary operators
        System.out.println("Uninary operators:");
        int i=0;
        i++;
        System.out.println(i);
        i--;
        System.out.println(i);
        ++i;
        System.out.println(i);
        --i;
        System.out.println(i);
    }
}
